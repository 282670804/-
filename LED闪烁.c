/*-----------------------------------------------
             向595写入一个字节
------------------------------------------------*/
void InputByte(unsigned  char dat) //写一个字节
{ 
  unsigned char i; 
        
   for(i=0;i<8;i++)
        {
         
         T_IO=!(dat&0x01);
         dat>>=1;
         T_CLK=0;
         T_CLK=1;
         }
}

/*-----------------------------------------------
                    延时程序
------------------------------------------------*/

void Delay(unsigned int t)
{
 while(--t);
}






